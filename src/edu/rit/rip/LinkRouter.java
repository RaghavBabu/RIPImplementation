package edu.rit.rip;

/**
 * Class LinkRouter
 * Class holds information of link router.
 * @author Raghav Babu
 * @version 5-Oct-2015
 */
public class LinkRouter {
	
	private String routerName;
	private int cost;
	
	public String getRouterName() {
		return routerName;
	}
	public void setRouterName(String routerName) {
		this.routerName = routerName;
	}
	public int getCost() {
		return cost;
	}
	public void setCost(int cost) {
		this.cost = cost;
	}
	

}
